
/* the following are the doodle command definition numbers */

#define D_END       -10 /* end of data list */
#define D_BOX       1   /* rectangle */
#define D_ELLIPSE   2   /* ellipse (or circle) */
#define D_TEXT      3   /* text command strings */
#define D_POLYLINE  4   /* line sequence */
#define D_POLYGON   5   /* polygon sequence */
#define D_ARC_PTS   6   /* arc by three points */
#define D_ARC_CENT  7   /* arc by center and radius */
#define D_BOX_INCLIP    8  /* box clip inside */
#define D_BOX_OUTCLIP   9  /* box clip outside */
#define D_ELL_INCLIP   10  /* ellipse clip inside */
#define D_ELL_OUTCLIP  11  /* ellipse clip outside */
#define D_POLY_INCLIP  12  /* polygon clip inside */
#define D_POLY_OUTCLIP 13  /* polygon clip outside */
#define D_NO_INCLIP    14  /* turn of inside clipping */
#define D_NO_OUTCLIP   15  /* turn of outside clipping */
#define D_LINE_SPLINE  16  /* line spline */
#define D_POLY_SPLINE  17  /* closed spline */
#define D_SPLINE_INCLIP  18 /* spline clip inside */
#define D_SPLINE_OUTCLIP 19 /* spline clip inside */
